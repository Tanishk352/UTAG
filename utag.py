# Create a TelegramClient object
client = TelegramClient('UTAG', API_ID, API_HASH)

def mention_all_with_random_message():
    with client:
        # Authorize the client
        client.start(phone=PHONE_NUMBER)
        
        # Get the usernames of all members in the supergroup
        for member in client.iter_participants(SUPERGROUP_LINK):
            if member.username:
                # Select a random message
                random_message = random.choice(RANDOM_MESSAGES)
                
                # Replace "{username}" in the message with the member's username
                message = random_message.format(username=member.username)
                
                # Try sending the message, handle FloodWaitError
                try:
                    client(SendMessageRequest(SUPERGROUP_LINK, message))
                except FloodWaitError as e:
                    # If FloodWaitError occurs, wait for the specified duration and retry
                    wait_time = e.seconds
                    print(f"FloodWaitError: Waiting for {wait_time} seconds...")
                    time.sleep(wait_time)
                    client(SendMessageRequest(SUPERGROUP_LINK, message))

# Run the main function
mention_all_with_random_message()
